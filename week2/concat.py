#CONCATENATION OF STRINGS
word=input("Enter a word to display them in Upper case: ")
word=word.upper()                                           #.upper() is the function used to concatenate strings into upper case.
print("Given word in upper case: ",word)
char=input("Enter another word to display them in lower case: ")
char=char.lower()                                           #.lower() is the function used to concatenate strings into lower case.
print("Given word in lower case: ",char)
